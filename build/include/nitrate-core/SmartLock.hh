#ifndef __NITRATE_CORE_SMART_LOCK_H__
#define __NITRATE_CORE_SMART_LOCK_H__

#include <atomic>

namespace ncc {
  /* Enable to make the libraries thread-safe. */
  extern std::atomic<bool> EnableSync;

  template <typename T>
  class SmartLock {
    T &m_mutex;
    bool m_enable_sync;

  public:
    SmartLock(T &m) noexcept : m_mutex(m), m_enable_sync(EnableSync.load(std::memory_order_relaxed)) {
      if (m_enable_sync) {
        m_mutex.lock();
      }
    }

    ~SmartLock() noexcept {
      if (m_enable_sync) {
        m_mutex.unlock();
      }
    }

    [[nodiscard]] auto IsEnabled() const noexcept -> bool { return m_enable_sync; }
  };
}  // namespace ncc

#endif
